public class Excercice_diapo240 {

	public static void main(String[] args) {
		for (int i = 1; i< 6; i++) {
			if (i == 3) {
				break;
			}
			System.out.println("BUCLE FOR");
		}
		int j = 0;
		
		while (j < 10) {
			j++;{
				if (j == 3) {
					break;
				}
			System.out.println("BUCLE WHILE");
			}
		}
	}

}
