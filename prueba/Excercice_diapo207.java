package prueba;

public class Excercice_diapo207 {

	public static void main(String[] args) {
		int num1 = 1, num2 = 2, num3 = 3, num4 = 4;
		
		System.out.println(num4 +" Es igual que " + num2 + "?" + (num4 == num2));
		System.out.println(num2 +" Es igual que " + num3 + "?" + (num2 == num3));
		System.out.println(num1 +" Es distinto que " + num3 + "?" + (num1 != num3));
		System.out.println(num2 +" Es distinto que " + num2 + "?" + (num2 != num2));
		System.out.println(num4 +" Es menor que " + num3 + "?" + (num4 < num3));
		System.out.println(num3 +" Es menor que " + num1 + "?" + (num3 < num1));
		System.out.println(num1 +" Es mayor que " + num3 + "?" + (num1 > num3));
		System.out.println(num2 +" Es mayor que " + num4 + "?" + (num2 > num4));
		System.out.println(num2 +" Es mayor que " + num3 + "?" + (num2 > num3));
		System.out.println(num2 +" Es igual o menor que " + num3 + "?" + (num2 <= num3));
		System.out.println(num2 +" Es igual o menor que " + num4 + "?" + (num2 <= num4));
		System.out.println(num2 +" Es igual o menor que " + num1 + "?" + (num2 <= num1));
		System.out.println(num1 +" Es igual o mayor que " + num2 + "?" + (num1 >= num2));
		System.out.println(num2 +" Es igual o mayor que " + num1 + "?" + (num2 >= num1));
		System.out.println(num2 +" Es igual o mayor que " + num2 + "?" + (num2 >= num2));
	}

}
